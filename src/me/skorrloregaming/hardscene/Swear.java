package me.skorrloregaming.hardscene;

public class Swear {

	private int arg0, arg3;
	private boolean arg1;
	private String arg2;

	public Swear(int arg0, boolean arg1, String arg2, int arg3) {
		this.setArg0(arg0);
		this.setArg1(arg1);
		this.setArg2(arg2);
		this.setArg3(arg3);
	}

	public int getArg0() {
		return arg0;
	}

	public void setArg0(int arg0) {
		this.arg0 = arg0;
	}

	public boolean getArg1() {
		return arg1;
	}

	public void setArg1(boolean arg1) {
		this.arg1 = arg1;
	}

	public String getArg2() {
		return arg2;
	}

	public void setArg2(String arg2) {
		this.arg2 = arg2;
	}

	public int getArg3() {
		return arg3;
	}

	public void setArg3(int arg3) {
		this.arg3 = arg3;
	}

}
